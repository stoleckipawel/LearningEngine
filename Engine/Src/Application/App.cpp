#include "Core/PCH.h"
#include "App.h"
#include "Platform/Window.h"
#include "Renderer/Renderer.h"
#include "Platform/Input/InputSystem.h"
#include "Scene/Scene.h"
#include "GameEngine/Camera/CameraController.h"
#include "Assets/AssetSystem.h"
#include "RHI/D3D12/D3D12Rhi.h"
#include "Core/Time/Timer.h"

#include <utility>

App::App(std::string windowTitle) : m_windowTitle(std::move(windowTitle)) {}

App::~App() = default;

void App::Run()
{
	Initialize();
	RenderLoop();
	Shutdown();
}

void App::BeginFrame()
{
	m_inputSystem->BeginFrame();
	m_window->PollEvents();
	m_inputSystem->ProcessDeferredEvents();
	m_cameraController->Update();
}

void App::EndFrame()
{
	m_inputSystem->EndFrame();
}

void App::RenderLoop()
{
	while (!m_window->ShouldClose())
	{
		BeginFrame();

		m_renderer->OnRender();

		EndFrame();
	}
}

void App::Initialize()
{
	m_timer = std::make_unique<Timer>();

	m_assetSystem = std::make_unique<AssetSystem>();

	m_rhi = std::make_unique<D3D12Rhi>();

	m_window = std::make_unique<Window>(m_windowTitle);

	m_inputSystem = InputSystem::Create();
	m_inputSystem->SubscribeToWindow(*m_window);

	m_scene = std::make_unique<Scene>();
	m_scene->GetMeshFactory().Upload(*m_rhi);

	m_cameraController = std::make_unique<CameraController>(*m_timer, *m_inputSystem, *m_window, m_scene->GetCamera());

	m_renderer = std::make_unique<Renderer>(*m_timer, *m_assetSystem, *m_rhi, *m_scene, *m_window);
}

void App::Shutdown()
{
	m_renderer.reset();
	m_cameraController.reset();
	m_scene.reset();
	m_inputSystem.reset();
	m_window.reset();
	m_rhi.reset();
	m_assetSystem.reset();
	m_timer.reset();
}
